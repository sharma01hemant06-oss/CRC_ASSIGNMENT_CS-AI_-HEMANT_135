import java.util.*;
class InputExample
{
    public static void main(String[] args){
        Scanner sc=new Scanner(System.in);
        int a=sc.nextInt();
        float b=sc.nextFloat();
        char c=sc.next().charAt(0);
        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
    }
}
